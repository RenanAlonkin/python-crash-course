# In range, the first number is where to start
# and the second number is where to stop
for value in range(1,5):
    print(value)

print("------------------------")

# It will start on 1, and end on 5
for value in range(1,6):
    print(value)

print("------------------------")

# Saving a range into a list
numbers = list(range(1,6))
print(numbers)

print("------------------------")

digits = [1,2,3,4,5,6,7,8,9,0]
# Shows the minimum value
print(min(digits))
# Shows the maximum value
print(max(digits))
# Shows the sum of all values
print(sum(digits))

