# 3.8 Discovering the world

# Store the elements in a list
places_i_want_to_visit = ['Belarus', 'Uruguay', 'Russia', 'South Korea', 'Canada']

# Original order
print(places_i_want_to_visit)

# Show the list sorted without altering the original order
print(sorted(places_i_want_to_visit))

# Original order
print(places_i_want_to_visit)

# Reversing the order
places_i_want_to_visit.reverse()
print(places_i_want_to_visit)

# Turn back to the original order
places_i_want_to_visit.reverse()
print(places_i_want_to_visit)

# Sorting it alphabetically
places_i_want_to_visit.sort()
print(places_i_want_to_visit)

# Sorting it backwards
places_i_want_to_visit.sort(reverse=True)
print(places_i_want_to_visit)

