# Lists are created in python by using the []
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles)

# Accessing individual elements of a list
print(bicycles[0])

# We can use specific methods for it's original data type
print(bicycles[0].title())

# Note: python considers 0 as the initial index

# Python also have a special syntax to get the last element
print(bicycles[-1])